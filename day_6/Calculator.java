public class Calculator {

  public int add(int n, int m) {
    return n + m;
  }

  public int subtract(int n, int m) {
    return n - m;
  }

  public int multiply(int n, int m) {
    return n * m;
  }

  public double divide(int n, int m) {
    return (double) n / (double) m;
  }

  public int modulus(int n, int m) {
    return n % m;
  }

}
